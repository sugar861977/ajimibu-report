'use strict';

var fs = require('fs');
var path = require('path');
var print = console.log.bind(console);

module.exports = {
  printLicense: function() {
    // This reads the license header from the current file.
    var data = fs.readFileSync(__filename, 'utf-8');
    data.split(/\n/).every(function(line) {
      if (line.indexOf('//') === 0) {
        print(line);
        return true;
      }
      return false;
    });
    print();
  },

  printAutoGenerated: function() {
    print('// This file was auto generated by %s',
                path.basename(process.argv[1]));
    print('// from %s', path.basename(process.argv[2]));
    print('// Do not edit!');
    print();
  }
};
